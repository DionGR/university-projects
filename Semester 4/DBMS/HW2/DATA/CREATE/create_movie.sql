create table Movie(
   id int,
   adult varchar(10),
   budget int,
   homepage varchar(230),
   original_language varchar(10),
   original_title varchar(110),
   title varchar(110),
   tagline varchar(280),
   overview varchar(1000),
   popularity varchar(10),
   release_date date,
   revenue int,
   runtime varchar(10),
   primary key (id)
);
